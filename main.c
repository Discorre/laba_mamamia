void z30() // Находит простые числа и выводит выбранное пользователем колличество
{
	printf("Находит простые числа и выводит выбранное пользователем колличество \n");
	int i, a, v, m, s, j; // a - число, которое должно оказаться простым, s - защёлка
	int A[100];
	printf("Введите число: ");
	scanf("%d", &v); // v - число, в границах которого мы ищем простые числа
	printf("Введите колличество простых чисел: ");
	scanf("%d", &m); // m - кол-во простых чисел, которое мы выведем в границах v
	for (int x = 0; x < 100; x++) // заполняем массив  числами до 100 (массив делителей)
		A[x] = x;
	for (i = 0, a = 2; a < v && i <= m - 1; a++) // Перебор чисел
	{
		for (s = 0, j = 0; j < i; j++) // Цикл проверки на делимость
			if (a % A[j] == 0)
			{
				s = 1; // s - Переменная-признак 
				break;
			}
		if (s == 0) // Если первое условие в цикле не выполнилось, то s будет равно 0, следовательно оно простое
			A[i++] = a; // И мы заносим его в конечный массив простых чисел
	}
	A[i] = 0; // Флаг для вывода
	int x = 0;
	while (A[x] != 0)
	{
		printf("%d ", A[x]); // Проходимся по каждому индексу массива и выводим его, пока нам не встретися 0.
		x++;
	}
}

