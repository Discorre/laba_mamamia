void z28() // Поиск Наибольшего общего делителя
{
	printf("Поиск Наибольшего общего делителя \n");
	int v, i, s, n; // v - делитель
	int A[10];
	for (int x = 1; x < 11; x++) // заполняем массив числами 10, 20, 30, ..., 1000
		A[x - 1] = x * 10;
	printf("Введите число: ");
	scanf("%d", &n);  // n - Предел длины массива A 
	v = A[0] + 1; // Чтобы цикл выполнился без изменения результата, добавляем 1
	do {
		v--;
		for (i = 0, s = 0; i < n; i++) // Перебор всех потенциальных делителей для чисел в массиве
			if (A[i] % v != 0) // Проверка на делимость
			{
				s = 1; // s - переменная-признак
				break;
			}

	} while (s == 1); // << Пока s = 1, если s = 0, то мы нашли НОД и смысла продолжать нету
	printf("%d", v); // Нахождение наибольшего общего делителя
}

